/**
 * Bake Supabase anon credentials into the Electron-bundled Express server
 * at build time so the packaged app needs no runtime env file.
 *
 * Anon keys are public by design — they ship to browsers and desktop apps,
 * and Supabase RLS is what enforces per-user isolation. Service-role keys
 * or other real secrets must NEVER be written here.
 *
 * Inputs (from process.env at build time):
 *   SUPABASE_URL      | VITE_SUPABASE_URL
 *   SUPABASE_ANON_KEY | VITE_SUPABASE_ANON_KEY
 *
 * Output:
 *   src/server/env.generated.js — gitignored; copied into resources/server/
 *   by electron-builder's extraResources rule and consumed by load-env.js.
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const outPath = path.join(__dirname, '..', '..', 'src', 'server', 'env.generated.js')

const url = process.env.SUPABASE_URL || process.env.VITE_SUPABASE_URL
const anon = process.env.SUPABASE_ANON_KEY || process.env.VITE_SUPABASE_ANON_KEY

if (!url || !anon) {
  console.error(
    'generate-server-env: SUPABASE_URL and SUPABASE_ANON_KEY must be set in the build environment.\n' +
      '  Local: run `npx vercel env pull .env.local` and re-run the build from a shell that loads it,\n' +
      '         or export the vars manually before `npm run electron:build`.\n' +
      '  CI:    set them in the project / CI secret store.'
  )
  process.exit(1)
}

const body =
  '// Generated by electron/scripts/generate-server-env.js — DO NOT EDIT, DO NOT COMMIT.\n' +
  `export const bakedEnv = ${JSON.stringify({ SUPABASE_URL: url, SUPABASE_ANON_KEY: anon }, null, 2)}\n`

fs.writeFileSync(outPath, body, 'utf8')
console.log(`generate-server-env: wrote ${path.relative(process.cwd(), outPath)}`)
