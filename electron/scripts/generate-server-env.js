/**
 * Bake credentials into the Electron-bundled Express server at build time so
 * the packaged app needs no runtime env file.
 *
 * Supabase anon keys are public by design — they ship to browsers and desktop
 * apps, and RLS is what enforces per-user isolation.
 *
 * ANTHROPIC_API_KEY is a real secret. Baking it here is acceptable ONLY for
 * personal builds you do not distribute. If you plan to ship to other users,
 * move the key to a settings screen so each user supplies their own.
 *
 * Inputs — loaded automatically from .env.local files, or from process.env if
 * the vars are already exported:
 *   SUPABASE_URL      | VITE_SUPABASE_URL
 *   SUPABASE_ANON_KEY | VITE_SUPABASE_ANON_KEY
 *   ANTHROPIC_API_KEY (optional; syllabus parser is disabled if absent)
 *
 * Output:
 *   src/server/env.generated.js — gitignored; copied into resources/server/
 *   by electron-builder's extraResources rule and consumed by load-env.js.
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, '..', '..')
const outPath = path.join(ROOT, 'src', 'server', 'env.generated.js')

function parseEnvFile(filePath) {
  const out = {}
  if (!fs.existsSync(filePath)) return out
  for (let line of fs.readFileSync(filePath, 'utf8').split(/\r?\n/)) {
    line = line.trim()
    if (!line || line.startsWith('#')) continue
    const eq = line.indexOf('=')
    if (eq <= 0) continue
    const key = line.slice(0, eq).trim()
    if (!/^[A-Za-z_][A-Za-z0-9_]*$/.test(key)) continue
    let val = line.slice(eq + 1).trim()
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1).replace(/\\n/g, '\n').replace(/\\"/g, '"').replace(/\\'/g, "'")
    }
    out[key] = val
  }
  return out
}

// Load env files so the user doesn't need to manually export vars before
// running `npm run electron:build`. Process.env always wins (CI / explicit
// exports take precedence over local files).
const fromRootLocal = parseEnvFile(path.join(ROOT, '.env.local'))
const fromServerLocal = parseEnvFile(path.join(ROOT, 'src', 'server', '.env.local'))
for (const [k, v] of Object.entries({ ...fromRootLocal, ...fromServerLocal })) {
  if (process.env[k] === undefined) process.env[k] = v
}

const url = process.env.SUPABASE_URL || process.env.VITE_SUPABASE_URL
const anon = process.env.SUPABASE_ANON_KEY || process.env.VITE_SUPABASE_ANON_KEY
const anthropicKey = process.env.ANTHROPIC_API_KEY

if (!url || !anon) {
  console.error(
    'generate-server-env: SUPABASE_URL and SUPABASE_ANON_KEY must be set.\n' +
      '  Expected them in src/server/.env.local or the root .env.local, but neither was found.\n' +
      '  You can also export them manually before running `npm run electron:build`.'
  )
  process.exit(1)
}

if (!anthropicKey) {
  console.warn(
    'generate-server-env: ANTHROPIC_API_KEY not found — syllabus parser will be disabled in the packaged build.'
  )
}

// Belt-and-suspenders: refuse to bake anything other than an `anon`-role JWT
// into the shipped bundle. If a developer ever sources their shell from a
// .env that exports a service_role key into SUPABASE_ANON_KEY by mistake,
// this catches it before the elevated key reaches end users' machines.
function assertAnonJwt(token) {
  const parts = String(token).split('.')
  if (parts.length !== 3) {
    throw new Error('SUPABASE_ANON_KEY does not look like a JWT (expected three dot-separated segments).')
  }
  let payload
  try {
    const b64 = parts[1].replace(/-/g, '+').replace(/_/g, '/')
    payload = JSON.parse(Buffer.from(b64, 'base64').toString('utf8'))
  } catch (err) {
    throw new Error(`SUPABASE_ANON_KEY payload is not valid base64-url JSON: ${err.message}`)
  }
  if (payload.role !== 'anon') {
    throw new Error(
      `SUPABASE_ANON_KEY has role="${payload.role}", refusing to ship. ` +
        'Only the anon-role JWT may be baked into the desktop bundle; service_role keys bypass RLS.'
    )
  }
}

try {
  assertAnonJwt(anon)
} catch (err) {
  console.error(`generate-server-env: ${err.message}`)
  process.exit(1)
}

const baked = { SUPABASE_URL: url, SUPABASE_ANON_KEY: anon }
if (anthropicKey) baked.ANTHROPIC_API_KEY = anthropicKey

const body =
  '// Generated by electron/scripts/generate-server-env.js — DO NOT EDIT, DO NOT COMMIT.\n' +
  `export const bakedEnv = ${JSON.stringify(baked, null, 2)}\n`

fs.writeFileSync(outPath, body, 'utf8')
console.log(`generate-server-env: wrote ${path.relative(process.cwd(), outPath)}`)
